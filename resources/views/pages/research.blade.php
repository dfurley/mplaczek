			underlie complex human pathological conditions, including stress and eating disorders. Our
			goal is therefore to understand how the hypothalamus develops in the embryo and how the
			proper embryonic assembly of the hypothalamus holds the key to robust adult function. We
			focus in particular on five key areas:
			<ul class="list">
